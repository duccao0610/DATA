public class ThanhVien {
    private int anh;
    private String ten;

    public ThanhVien(int anh, String ten) {
        this.anh = anh;
        this.ten = ten;
    }

    public int getAnh() {
        return anh;
    }

    public String getTen() {
        return ten;
    }
}
